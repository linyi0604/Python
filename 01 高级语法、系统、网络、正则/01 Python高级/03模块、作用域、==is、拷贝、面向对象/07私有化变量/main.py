'''
在类当中，我们把变量或者方法名写成__name 形成了私有化变量，这样的变量不能在外部或者子类中访问
    其实实际上是发生了 名字重整，变成了 _类名__name   其实在外部也是能够访问的 但是不推荐这样访问
'''
class Foo(object):
    def __init__(self):
        self.__a=10
print("获取私有属性："+"-"*50)
f = Foo()
# print( f.__a ) #会报异常 找不到这个属性
print( f._Foo__a )  # 这是__a 私有化变量被重整名字之后的名字 不推荐的访问方式

'''
在导入的模块当中，_name 的变量和方法是没有办法导入的 这里设置了私有
'''
